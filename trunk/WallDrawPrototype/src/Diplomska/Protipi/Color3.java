public class Color3 {
    public float r, g, b;

    public Color3()
    {
        this.r  = this.g = this.b = 0.0f;
    }

    public Color3(float r, float g, float b)
    {
        this.r = r;
        this.g = g;
        this.b = b;
    }

}
