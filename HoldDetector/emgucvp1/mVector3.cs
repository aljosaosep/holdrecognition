using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace HoldDetector
{
    public class mVector3
    {
        public int x, y, z;

        public mVector3()
        {
            x = 0;
            y = 0;
            z = 0;
        }

        public mVector3(int x, int y, int z)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
